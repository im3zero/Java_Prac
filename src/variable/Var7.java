package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // int 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언: t, f 가능
        char d = 'A'; // 문자 하나!
        String e = "Hello"; // 유일하게 대문자로 시작, 문자열, 문자열을 다루기위한 특별한 타임

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
